#1. write a program to demonstrate basic data type in python

# integer
x=20
print(f"x={x}")
print(type(x))

#float
y=20.5
print(f"y={y}")
print(type(y))

#string
str="hello world to the world of python"
print(f"string={str}")
print(type(str))

#list
li=[1,"hi",4.5]
print(f"list={li}")
print(type(li))

#Tuple
tup=(1,"python",3.9)
print(f"tuple={tup}")

#Dictionary
dict={"Name":"Abhishek","Age":23,"course":"BCA"}
print("Dictionary=",dict)
print(type(dict))

#set
s={1,2,3,4,5}
print(f"set={s}")
print(type(s))

#bool
b=True
print(f"Bollen={b}")
print(type(b))