class Vishwa
{
    public static void main(String []args)
    {
        int a1=10;
        int a2=20;
        System.out.println("a1&a2  "+(a1&a2));//*
        System.out.println("a1|a2  "+(a1|a2));//+
        System.out.println("a1^a2  "+(a1^a2));//XOR
        System.out.println("~a1  "+(~a1));//reverse true->false
        
    }
}

